package 数组;
/*
给定一位研究者论文被引用次数的数组（被引用次数是非负整数）。编写一个方法，计算出研究者的 h 指数。
h 指数的定义: “h 代表“高引用次数”（high citations），一名科研人员的 h 指数是指他（她）的 （N 篇论文中）
至多有 h 篇论文分别被引用了至少 h 次。（其余的 N - h 篇论文每篇被引用次数不多于 h 次。）”

输入: citations = [3,0,6,1,5]
输出: 3 
解释: 给定数组表示研究者总共有 5 篇论文，每篇论文相应的被引用了 3, 0, 6, 1, 5 次。
由于研究者有 3 篇论文每篇至少被引用了 3 次，其余两篇论文每篇被引用不多于 3 次，所以她的 h 指数是 3。

如果 h 有多种可能的值，h 指数是其中最大的那个。
 */

public class h指数 {
    public int hIndex(int[] citations) {
        int len=citations.length;
        int max=len; //h的最大值
        int min=0; //h的最小值
        int res=0;//最终的结果
        while (min<=max) {
			int mid=(min+max)/2;
			int low=0;//记录引用次数大于等于mid次的次数
			int high=0; //记录引用次数小于mid次的次数
			for (int i = 0; i < len; i++) {
				if (citations[i]>=mid) {
					high++;
				}else {
					low++;
				}
			}
			if (high>=mid && low<=len-mid) {
				if (mid>res) {
					res=mid;
				}
				min=mid+1;
			}else {
				max=mid-1;
			}
		}
        return res;
    }
}
